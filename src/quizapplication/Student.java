    private void tableStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableStudentMouseClicked

        ClearForm();
        //disable Save
        btnSave.setEnabled(false);

        DefaultTableModel tm = (DefaultTableModel) tableStudent.getModel();

        int selindex = tableStudent.getSelectedRow();

        sid = tm.getValueAt(selindex, 0).toString();

        textSname.setText(tm.getValueAt(selindex, 1).toString());
        textPhone.setText(tm.getValueAt(selindex, 2).toString());
        textAge.setText(tm.getValueAt(selindex, 3).toString());
        textAddress.setText(tm.getValueAt(selindex, 4).toString());

        

    }//GEN-LAST:event_tableStudentMouseClicked

