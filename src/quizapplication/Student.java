    public void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String sname = textSname.getText();
        String age = textAge.getText();
        String phone = textPhone.getText();
        String address = textAddress.getText();
        

        try {

            con = DBConnection.connecttoDB();
            ps = con.prepareStatement("insert into Students(fullname,phone,age,address) VALUES(?,?,?,?)");
            ps.setString(1, sname);
            ps.setString(2, phone);
            ps.setString(3, age);
            ps.setString(4, address);
            int msg = ps.executeUpdate();

            if (msg >= 1) {
                JOptionPane.showMessageDialog(null, "record inserted successfully ...");
            } else if (msg == 0) {
                JOptionPane.showMessageDialog(null, "error occured !");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }

        ClearForm();
        btnRefresh.doClick();
    }//GEN-LAST:event_btnSaveActionPerformed

    public void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed

        btnRefresh.setText("Wait...");
        dtm = (DefaultTableModel) tableStudent.getModel();
        dtm.setRowCount(0); //clear rows

        try {
            res = returnRecords();
            int i = 0;
            while (res.next()) {

                dtm.insertRow(i, new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),}
                );
            }
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }

        tableStudent.setModel(dtm);
               
        btnRefresh.setText("Refresh");

    }//GEN-LAST:event_btnRefreshActionPerformed

    
