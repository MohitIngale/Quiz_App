    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed

        try {
            sID = textsID.getText().toString();
            cat = textCat.getText().toString();
            tno = textTestno.getText().toString();
            sName = textSName.getText().toString();
        } catch (Exception ex) {

        }
        if (!sID.isEmpty()) {
            textSName.setEnabled(false);
            textTestno.setEnabled(false);
            textCat.setEnabled(false);
            dtm.setRowCount(0);
            returnResults("select * from examresults WHERE student_id=" + sID);
        }

        if (!cat.isEmpty()) {
            textSName.setEnabled(false);
            textTestno.setEnabled(false);
            textsID.setEnabled(false);
            dtm.setRowCount(0);
            returnResults("select * from examresults WHERE test_cat='" + cat + "'");
        }
        if (!tno.isEmpty()) {
            textSName.setEnabled(false);
            textsID.setEnabled(false);
            textCat.setEnabled(false);
            dtm.setRowCount(0);
            returnResults("select * from examresults WHERE test_no='" + tno + "'");
        }
        if (!sName.isEmpty()) {
            textsID.setEnabled(false);
            textTestno.setEnabled(false);
            textCat.setEnabled(false);
            dtm.setRowCount(0);
            returnResults("select * from examresults WHERE student_name='" + sName + "'");
        }


    }//GEN-LAST:event_btnFilterActionPerformed

