    public void returnResults(String query) {

        try {
            con = DBConnection.connecttoDB();
            Statement s = con.createStatement();
            res = s.executeQuery(query);

            if (!res.next()) {
                JOptionPane.showMessageDialog(null, "No records found ...");
            }
            res.beforeFirst();

            int i = 0;
            while (res.next()) {

                dtm.insertRow(i, new String[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),
                    res.getString(6),
                    res.getString(7)

                });

            }

            s.close();
            con.close();

        } catch (SQLException ex) {
            Logger.getLogger(ShowExamResults.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

