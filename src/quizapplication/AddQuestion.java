    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        if (!textAreaQuestion.getText().isEmpty()
                && !textA.getText().isEmpty()
                && !textB.getText().isEmpty()
                && !textC.getText().isEmpty()
                && !textD.getText().isEmpty()
                && !textAnswer.getText().isEmpty()) {
            try {

                con = DBConnection.connecttoDB();
                ps = con.prepareStatement("insert into questions(q_no,q_text,q_opt1,q_opt2,q_opt3,q_opt4,q_cor_ans,q_cat,test_no) "
                        + "VALUES(?,?,?,?,?,?,?,?,?)");

                ps.setString(1, lblQno.getText().toString());
                ps.setString(2, textAreaQuestion.getText().toString());
                ps.setString(3, textA.getText().toString());
                ps.setString(4, textB.getText().toString());
                ps.setString(5, textC.getText().toString());
                ps.setString(6, textD.getText().toString());
                // A means q_opt1
                ps.setString(7, textAnswer.getText().toString());
                ps.setString(8, comboQCat.getSelectedItem().toString());
                ps.setString(9, textTestno.getText().toString());

                int msg = ps.executeUpdate();
                if (msg >= 1) {
                    JOptionPane.showMessageDialog(null, "Question saved successfully ...");

                } else {
                    JOptionPane.showMessageDialog(null, "Error occured !");

                }

                res.close();
                con.close();

            } catch (SQLException ex) {
                Logger.getLogger(AddQuestion.class.getName()).log(Level.SEVERE, null, ex);
            }

            clearForm();
            generateAutoID();

        } else {
            JOptionPane.showMessageDialog(null, "Please fill all the fields ! ");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

