    public void loadCategories() {

        try {
            con = DBConnection.connecttoDB();
            ps = con.prepareStatement("select * from cat");
            res = ps.executeQuery();

            if (!res.next()) {

                JOptionPane.showMessageDialog(null, "No record found ...");

            } else {
                res.beforeFirst();

                comboQCat.removeAllItems();
                //add empty item
                comboQCat.addItem("None");

                while (res.next()) {

                    comboQCat.addItem(res.getString(2));
                }

            }

            ps.close();
            res.close();
            con.close();

        } catch (SQLException ex) {
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

