    public void generateAutoID() {
        clearForm();

        if (!textTestno.getText().isEmpty() && comboQCat.getSelectedItem() != "None") {
            cat = comboQCat.getSelectedItem().toString();
            try {

                con = DBConnection.connecttoDB();
                Statement s = con.createStatement();
                res = s.executeQuery("select max(q_no) from questions WHERE q_cat='" + cat + "' AND test_no='" + textTestno.getText().toString() + "'");
                res.next(); //cursor to 1 line below
                int id = res.getInt(1) + 1;
                lblQno.setText(String.valueOf(id));

                res.close();
                con.close();

            } catch (SQLException ex) {
                Logger.getLogger(AddQuestion.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }

