    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
           try {

            con = DBConnection.connecttoDB();
            ps = con.prepareStatement("delete from questions WHERE id=?");
            ps.setString(1, lblQID.getText().toString());
            int msg = ps.executeUpdate();

            if (msg >= 1) {
                JOptionPane.showMessageDialog(null, "record deleted successfully ...");
            } else if (msg == 0) {
                JOptionPane.showMessageDialog(null, "error occured !");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        btnRefresh.doClick();
        clearForm();
        
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
       
        con= DBConnection.connecttoDB() ;
        
        dtm = (DefaultTableModel)tableQuestion.getModel() ;
        dtm.setRowCount(0); //clear rows
        dtm.setColumnIdentifiers(new String[]{
        "id",
        "no",
        "question",
        "opt1",
        "opt2",
        "opt3",
        "opt4",
        "correct answer",
        "category",
        "test no"
        });
        
        
        
        try {
          
            if(textFilterByTestno.getText().isEmpty())
            ps  = con.prepareStatement("select * from questions WHERE q_cat='"+comboQCat.getSelectedItem().toString()+"'");
            else
            ps  = con.prepareStatement("select * from questions WHERE q_cat='"+comboQCat.getSelectedItem().toString()+"' AND test_no='"+textFilterByTestno.getText().toString()+"'");
      
            
            res = ps.executeQuery();
        
            int i = 0;
            
            if(!res.next())
                JOptionPane.showMessageDialog(null, "No records found ...");
            
            res.beforeFirst();
            
            while(res.next()){
                
              dtm.insertRow(i, new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),
                    res.getString(6),
                    res.getString(7),
                    res.getString(8),
                    res.getString(10),
                    res.getString(11),
                   }
                );
            }
        
           ps.close();
           res.close();
           con.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(EditQuestion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        tableQuestion.setModel(dtm);
        
    }//GEN-LAST:event_btnRefreshActionPerformed

