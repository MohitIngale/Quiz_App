    private void tableQuestionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableQuestionMouseClicked
       
        int selindex = tableQuestion.getSelectedRow();
        
       DefaultTableModel dm = (DefaultTableModel) tableQuestion.getModel();
       String qid = dm.getValueAt(selindex, 0).toString();
       String qno = dm.getValueAt(selindex, 1).toString();
       String qtext = dm.getValueAt(selindex, 2).toString();
       String qopt1 = dm.getValueAt(selindex, 3).toString();
       String qopt2 = dm.getValueAt(selindex, 4).toString();
       String qopt3 = dm.getValueAt(selindex, 5).toString();
       String qopt4 = dm.getValueAt(selindex, 6).toString();
       String qans = dm.getValueAt(selindex, 7).toString();
       String qcat = dm.getValueAt(selindex, 8).toString();
       String testno = dm.getValueAt(selindex, 9).toString();
       
      
       lblQID.setText(qid);
       lblQno.setText(qno);
       textAreaQuestion.setText(qtext);
       textA.setText(qopt1);
       textB.setText(qopt2);
       textC.setText(qopt3);
       textD.setText(qopt4);
       textAnswer.setText(qans);
       comboQCat.setSelectedItem(qcat);
       lblTestNo.setText(testno);
       
       
        
    }//GEN-LAST:event_tableQuestionMouseClicked

